function StarMarker({ x, y, fill = "#ffffff", size = 10 }: { x: number; y: number; fill?: string; size?: number }) {
  const points = [
    `${x},${y - size}`,
    `${x + size * 0.3},${y - size * 0.3}`,
    `${x + size},${y - size * 0.1}`,
    `${x + size * 0.4},${y + size * 0.25}`,
    `${x + size * 0.6},${y + size}`,
    `${x},${y + size * 0.5}`,
    `${x - size * 0.6},${y + size}`,
    `${x - size * 0.4},${y + size * 0.25}`,
    `${x - size},${y - size * 0.1}`,
    `${x - size * 0.3},${y - size * 0.3}`,
  ].join(" ");

  return <polygon points={points} fill={fill} />;
}

